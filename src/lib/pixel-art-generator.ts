// 픽셀아트를 Canvas에 그리는 함수
export function drawPixelArt(
  canvas: HTMLCanvasElement,
  config: PixelArtConfig,
  pixelSize: number = 4
): void {
  const ctx = canvas.getContext('2d');
  if (!ctx) return;

  canvas.width = config.width;
  canvas.height = config.height;

  // 배경을 투명하게
  ctx.clearRect(0, 0, canvas.width, canvas.height);

  // 픽셀아트 그리기
  for (let y = 0; y < config.pattern.length; y++) {
    for (let x = 0; x < config.pattern[y].length; x++) {
      const colorIndex = config.pattern[y][x];
      if (colorIndex > 0 && colorIndex < config.colors.length) {
        ctx.fillStyle = config.colors[colorIndex];
        ctx.fillRect(
          x * pixelSize,
          y * pixelSize,
          pixelSize,
          pixelSize
        );
      }
    }
  }
}

// 행성 유형별 픽셀아트를 Data URL로 변환
export function generatePlanetPixelArt(planetType: string): string {
  const config = PLANET_PIXEL_ART[planetType];
  if (!config) return '';

  const canvas = document.createElement('canvas');
  drawPixelArt(canvas, config);
  
  return canvas.toDataURL('image/png');
}
